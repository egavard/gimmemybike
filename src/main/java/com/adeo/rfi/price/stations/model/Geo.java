package com.adeo.rfi.price.stations.model;

public class Geo {
    private Double lat;
    private Double lon;

    public Geo() {
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return lon;
    }

    public void setLon(Double lon) {
        this.lon = lon;
    }
}
